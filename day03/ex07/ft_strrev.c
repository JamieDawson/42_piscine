char	*ft_strrev(char *str)
{
	int		i;
	int		l;
	char	temp;

	l = 0;
	while (str[l] != '\0')
		l++;
	i = -1;
	while (++i < --l)
	{
		temp = str[i];
		str[i] = str[l];
		str[l] = temp;
	}
	return (str);
}
